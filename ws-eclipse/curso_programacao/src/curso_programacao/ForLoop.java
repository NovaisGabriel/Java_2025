package curso_programacao;

import java.util.Scanner;

public class ForLoop {

	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		
		int N = sc.nextInt();
		
		for (int i = 0;i<=N;i=i+1) {
			System.out.println(i);
		};
		
		
		// do while not so common
		
		

	}

}
